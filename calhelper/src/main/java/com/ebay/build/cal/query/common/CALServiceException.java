package com.ebay.build.cal.query.common;

public class CALServiceException extends Exception {

	private static final long serialVersionUID = 3208069850724505403L;

	public CALServiceException() {
		super();
	}

	public CALServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public CALServiceException(String message) {
		super(message);
	}

	public CALServiceException(Throwable cause) {
		super(cause);
	}
	
}
